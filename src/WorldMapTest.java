	@Test
	public void testGetNeighborCount() {
		 
		int count = worldMap.getNeighborCount(0, 0);
		assertEquals(2, count);
		
		count = worldMap.getNeighborCount(0, worldMap.cols - 1);
		assertEquals(2, count);
		
		count = worldMap.getNeighborCount(worldMap.rows - 1, 0);
		assertEquals(1, count);
		
		count = worldMap.getNeighborCount(worldMap.rows - 1, worldMap.cols - 1);
		assertEquals(1, count);
		
		count = worldMap.getNeighborCount(0, 1);
		assertEquals(3, count);
		
		count = worldMap.getNeighborCount(1, 0);
		assertEquals(1, count);
		
		count = worldMap.getNeighborCount(1, 1);
		assertEquals(2, count);
		
		count = worldMap.getNeighborCount(2, 2);
		assertEquals(6, count);
		
	}

